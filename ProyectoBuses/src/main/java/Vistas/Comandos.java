package Vistas;

/**
 * Interfaz Comandos que define un método execute().
 * Los implementadores de esta interfaz deben proporcionar una implementación del método execute().
 */
public interface Comandos {
    /**
     * Método execute() que representa la acción a ejecutar.
     * Implementadores de esta interfaz deben definir la lógica específica de la acción.
     */
    public void execute();
}
